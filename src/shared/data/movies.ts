// Get movie by index
export function getMovieByIndex(index: number): Movie {
  return movieDatabase[index % movieDatabase.length];
}

// Get daily movie based on date
export function getDailyMovie(date: Date = new Date()): { movie: Movie; dayNumber: number } {
  // Use a fixed start date for consistent day numbering
  const startDate = new Date('2026-02-04'); // Launch date
  const diffTime = date.getTime() - startDate.getTime();
  const dayNumber = Math.floor(diffTime / (1000 * 60 * 60 * 24));
  
  // Use day number as seed for movie selection
  const movieIndex = Math.abs(dayNumber) % movieDatabase.length;
  
  return {
    movie: movieDatabase[movieIndex],
    dayNumber: dayNumber + 1, // Day 1, Day 2, etc.
  };
}
