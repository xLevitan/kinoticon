const DEFAULT_START_DATE = '2026-02-04';

/** Day number (0-based) from a calendar date YYYY-MM-DD (user's local date = Wordle-style midnight). */
function dayNumberFromDate(dateStr: string, startDateStr: string): number {
  const toDate = (s: string) => {
    const [y, m, d] = s.split('-').map(Number);
    return new Date(Date.UTC(y ?? 0, (m ?? 1) - 1, d ?? 1));
  };
  const date = toDate(dateStr);
  const start = toDate(startDateStr);
  const diffMs = date.getTime() - start.getTime();
  return Math.floor(diffMs / (1000 * 60 * 60 * 24));
}

/** Get daily movie. startDate: YYYY-MM-DD â€” dev=today, release=persisted in Redis. */
export function getDailyMovie(
  override?: number | string,
  startDate = DEFAULT_START_DATE
): { movie: Movie; dayNumber: number } {
  let dayNumber: number;
  if (typeof override === 'number' && override > 0) {
    dayNumber = override - 1; // 1-based â†’ 0-based
  } else if (typeof override === 'string' && /^\d{4}-\d{2}-\d{2}$/.test(override)) {
    dayNumber = dayNumberFromDate(override, startDate);
  } else {
    dayNumber = dayNumberFromDate(new Date().toISOString().slice(0, 10), startDate);
  }

  const movieIndex = Math.max(0, dayNumber) % movieDatabase.length;
  const movie = movieDatabase[movieIndex] ?? movieDatabase[0];
  return {
    movie: movie!,
    dayNumber: dayNumber + 1,
  };
}
