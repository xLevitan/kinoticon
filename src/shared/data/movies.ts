const START_DATE = new Date('2026-02-04T00:00:00Z');

/** Day number (0-based) from a calendar date YYYY-MM-DD (user's local date = Wordle-style midnight). */
function dayNumberFromDate(dateStr: string): number {
  const [y, m, d] = dateStr.split('-').map(Number);
  const date = new Date(Date.UTC(y, m - 1, d));
  const diffMs = date.getTime() - START_DATE.getTime();
  return Math.floor(diffMs / (1000 * 60 * 60 * 24));
}

